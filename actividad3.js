//Actividad3-Clasificación de edades 
let edad = 15; // Esto se encarga para guardar la edad de una persona, es decir es la variable que almecena la edad
// condicional para clasificar la edad 
if (edad >= 13) {
    console.log("Niño");
} else if (edad >= 13 && edad <= 17) {
    console.log("Adolescente");
} else if (edad >= 18) {
    console.log("Adulto");
}